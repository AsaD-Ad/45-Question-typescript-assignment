let  places :string[] = ["Makkah","Amazon","Sahara desert","Antarctica","Madina"]
console.log(places);
// Alphabetical order
console.log([...places].sort());
// Original order
console.log(places);
// reverse alphabetical order
console.log([...places].sort().reverse());
// original order
console.log(places);
// reverse order
places.reverse()
console.log(places)
// again reverse
places.reverse()
console.log(places);
// alphabetical order
places.sort()
console.log(places);
places.sort().reverse()
console.log(places);


